t=(2,4,5,6,9,4,12,8,7,10)
print("Tuple is =",t)
print("slice t[ : :2 ]", t[ : : 2])

print("slice t[3 : : ]", t[3 : : ])

print("slice t[ : 5: ]", t[ : 5: ])

print("slice t[1 :7 ]", t[ 1:7])

print("slice t[-1 : :-1 ]", t[ -1: : -1])

print("slice t[-1 :-6 :-2 ]", t[ -1:-6 :-2 ])

print("slice t[ : -6:-1 ]", t[ :-6 :-1 ])
